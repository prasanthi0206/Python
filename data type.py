# Data Types 
# Data type tells about the type of data that a variable is holiding on.
# Data types are categorized into 5 types

# 1.Number
#int
a = 123
print(a)
print(type(a))
print(id(a))
a1 = -90
print(a1)
print(type(a1))
print(id(a1))

#float
b = 89.46
print(b)
print(type(b))
print(id(b))
b1 = -93.4
print(b1)
print(type(b1))
print(id(b1))

#complex
c = 9+6j
print(c)
print(type(c))
print(id(c))
c1 = -10+8j
print(c1)
print(type(c1))
print(id(c1))

# 2.Sequence
#string
d = 'ists'
print(d)
print(type(d))
print(id(d))
d1 = "ists"
print(d1)
print(type(d1))
print(id(d1))

#list
mylist = [10, 11.8, 6+5j, "hello"]
print(mylist)
print(type(mylist))
print(id(mylist))
print(mylist[3])

#tuple
mytuple = (12, 2.3, "hi", [1,2,3j])
print(mytuple)
print(type(mytuple))
print(id(mytuple))
print(mytuple)

# 3. Boolean
a = True
print(a)
print(type(a))
b = False
print(b)
print(type(b))


# 4.Set
myset = {1, 2.3, 4+5j, "hi", True, ("hello",5)}
print(myset)

# 5.Dictionary
mydict = {1:"ists", "branch":"ECE","location":"rjy"}
print(mydict)
print(type(mydict))

